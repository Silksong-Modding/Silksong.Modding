namespace Modding.enums
{
	/// <summary>
	///		All possible level to log to the console
	/// </summary>
	public enum LogLevel : byte
	{
		/// <summary>
		///		The info log level
		/// </summary>
		INFO,

		/// <summary>
		///		The warning log level
		/// </summary>
		WARNING,

		/// <summary>
		///		The error log level
		/// </summary>
		ERROR,

		/// <summary>
		///		The debug log level
		/// </summary>
		DEBUG,

		/// <summary>
		///		The fine log level
		/// </summary>
		FINE
	}
}
